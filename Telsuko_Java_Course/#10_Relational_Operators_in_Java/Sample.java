class Sample
{
    public static void main(String args[])
    { 
        // Relational operators
        int x = 6, y = 5;

        boolean result;

        result = x < y;
        System.out.println(result);
        
        result = x > y;
        System.out.println(result);

        x = 6; y =6;

        result = x <= y;
        System.out.println(result);
        
        result = x >= y;
        System.out.println(result);

        result = x == y;
        System.out.println(result);

        result = x != y;
        System.out.println(result);

    }
}