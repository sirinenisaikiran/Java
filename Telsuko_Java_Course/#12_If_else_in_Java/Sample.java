class Sample
{
    public static void main(String arg[])
    {
        // Conditional statements.
        int x = 12;
        int y = 8;

        if(x>y) // Curly braces are required you have more than one statement.
        {
            System.out.println(x);
            System.out.println("Thank you!");
        }
        else
            System.out.println(y);
    }
}