class Sample
{
    public static void main(String arg[])
    {
        // conditional statements - if, else if, else.
        int x = 12;
        int y = 20;
        int z = 9;

        if(x>y && x>z)
            System.out.println(x);
        else if (y>z)
            System.out.println(y);
        else
            System.out.println(z);
    }
}